class TestExportUsersJSON(unittest.TestCase):
    """Battery of unit tests to validate the JSON export function."""

    def test_export_json_error_type(self) -> None:
        """Ensures that invalid data types (like None) are blocked before writing."""
        with self.assertRaises(TypeError):
            export_encrypted_users(None, DUMMY_JSON)#NOSONAR

    @patch("builtins.open", new_callable=mock_open)
    @patch("json.dump")
    def test_export_json_success(self, mock_json_dump: Any, mock_file: Any) -> None:
        """Simulates writing to a file in memory to verify correct behavior without touching the real hard drive."""
        fake_database = [{"name": "Ana", "id": "234"}]
        
        # Call the function
        export_encrypted_users(fake_database, DUMMY_JSON)
        
        # 1. Verifies if open() was called with "w" (write) mode
        mock_file.assert_called_once_with(DUMMY_JSON, "w", encoding="utf-8")
        
        # 2. Verifies if json.dump() was called to format the list into the file
        mock_json_dump.assert_called_once_with(fake_database, ANY, indent=4)

    @patch("builtins.open")
    def test_export_json_io_error(self, mock_file: Any) -> None:
        """Forces an OS Error to check if the system handles file writing failures properly."""
        # We program the mock to explode with an OSError when someone tries to open it
        mock_file.side_effect = OSError("Simulated Disk Error: No space left on device")
        
        # We ensure our function lets the OSError bubble up properly
        with self.assertRaises(OSError):
            export_encrypted_users([{"name": "Ana", "id": "234"}], "dummy_database.json")


