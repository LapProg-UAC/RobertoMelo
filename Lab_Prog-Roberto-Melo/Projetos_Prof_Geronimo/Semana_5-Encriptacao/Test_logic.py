class TestEncryptDecryptIntegration(unittest.TestCase):
    """
    Integration test to validate the full encrypt → decrypt pipeline,
    mirroring the exact flow used in main.py.
    """

    def test_roundtrip_full_user_pipeline(self) -> None:
        """
        Simulates the complete pipeline used in the main program:
        encrypts user IDs and then decrypts them, verifying the
        final data matches the original.
        """
        original_users = [
            {"name": "Ana",   "id": 123456789},
            {"name": "João",  "id": 987654321},
            {"name": "Maria", "id": 555000111},
        ]
        keys = [3, 7, 2]

        encrypted = encrypt_users(original_users, keys)
        recovered = decrypt_users(encrypted, keys)

        self.assertEqual(recovered, original_users)

